package com.assignments ;
public class OneDimensionalArray 
{
 public static void main(String args[])
 {
	 int[] anArray;
	 anArray = new int[5];
	
	 anArray[0] = 12;
	 anArray[1] = 4;
	 anArray[2] = 5;
	 anArray[3] = 1;
	 anArray[4] = 2;
	 
	 System.out.println("First Element: "  + anArray[0]);
	 System.out.println("Second Element: " + anArray[1]);
	 System.out.println("Third Element: "  + anArray[2]);
	 System.out.println("Fourth Element: " + anArray[3]);
	 System.out.println("Fifth Element: "  + anArray[4]);
			 
	 
 }
}
